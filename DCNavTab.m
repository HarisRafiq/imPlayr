#import "DCNavTab.h"

@implementation DCNavTab

////////////////////////////////////////////////////////////////////////////////////////////////////
+(DCNavTab*)tabWithTitle:(NSString*)title image:(UIImage*)image selectedImage:(UIImage*)selectedImage
           selectedColor:(UIColor*)selectedColor viewController:(Class)vcClass
{
    DCNavTab *tab = [DCNavTab new];
    tab.vcClass = vcClass;
    tab.title = title;
    tab.image = image;
    tab.selectedImage = selectedImage;
    tab.selectedColor = selectedColor;
    return tab;
}
////////////////////////////////////////////////////////////////////////////////////////////////////
+(DCNavTab*)tabWithView:(UIView*)view viewController:(Class)vcClass
{
    DCNavTab *tab = [DCNavTab new];
    tab.vcClass = vcClass;
    tab.view = view;
    return tab;
}
////////////////////////////////////////////////////////////////////////////////////////////////////

@end
